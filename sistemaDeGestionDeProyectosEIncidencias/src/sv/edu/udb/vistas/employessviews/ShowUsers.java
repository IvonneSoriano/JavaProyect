    public void loadData() {
        switch (Session.employeeType) {
            case 1:
                empleados = ec.findEmployee(2, Session.deparmentId);
                break;

            case 3:
                empleados = ec.findEmployee(4, Session.deparmentId);
                break;

            case 5:
                empleados = ec.findEmployees();
                supervisores = ec.findSupervisors();
                break;
        }

    }

