    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
         if (txtUser.getText().equals("") || txtPassword.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Favor llenar todos los campos",
                    "Error - Campo vacio",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        Employee e = controller.findEmployee(txtUser.getText(), txtPassword.getPassword());
        
        if (!e.getUsername().equals(DAODefaults.NON_EXISTING_USER.getDefaultValue())) {
            if (!e.getPassword().equals(DAODefaults.NON_EXISTING_USER.getDefaultValue())) {
                JOptionPane.showMessageDialog(null, "Bienvenido: "
                        + e.getEmployeeName() + " "
                        + e.getEmployeeLastname());

                Session.logIn(e);
                new Contenedor().setVisible(true);
                this.dispose();
                return;
            }
            JOptionPane.showMessageDialog(null,
                    e.getUsername() + " favor verificar su contrase침a",
                    "Error - Password incorrecto",
                    JOptionPane.ERROR_MESSAGE);
            logger.warn("Contrase침a incorrecta en intento de LogIn para usuario: " + e.getUsername());

        } else {
            JOptionPane.showMessageDialog(null, "Error: " + e.getUsername(),
                    "Error - Usuario incorrecto",
                    JOptionPane.WARNING_MESSAGE);
            logger.warn("Usuario no existe - intento fallido de inicio de sesi칩n");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

